
# Left operand
num1 = 27.5

# Right operand
num2 = 2

# Arithmetic operations
print("Addition: %d" %(num1 + num2))
print("Subtraction: %d" %(num1 - num2))
print("Multiplication: %d" %(num1 * num2))
print("Division: %d" %(num1 / num2))
print("Floor Division: %d" %(num1 // num2))
print("Exponentiation: %d" %(num1 ** num2))
